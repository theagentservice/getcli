import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { parse } from "yaml";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..", "..");
const manifestsDir = path.join(repoRoot, "manifests");
const outputPath = path.join(repoRoot, "web", "src", "data", "registry.generated.ts");

function assertString(value, field, file) {
  if (typeof value !== "string" || value.length === 0) {
    throw new Error(`${file}: expected "${field}" to be a non-empty string`);
  }

  return value;
}

function assertStringArray(value, field, file) {
  if (!Array.isArray(value) || value.some((item) => typeof item !== "string")) {
    throw new Error(`${file}: expected "${field}" to be an array of strings`);
  }

  return value;
}

async function loadTools() {
  const entries = (await fs.readdir(manifestsDir))
    .filter((entry) => entry.endsWith(".yaml"))
    .sort();
  const seenIds = new Set();

  return Promise.all(
    entries.map(async (entry) => {
      const fullPath = path.join(manifestsDir, entry);
      const manifest = parse(await fs.readFile(fullPath, "utf8"));
      const id = assertString(manifest.id, "id", entry);

      if (seenIds.has(id)) {
        throw new Error(`${entry}: duplicate manifest id "${id}"`);
      }
      seenIds.add(id);

      return {
        id,
        name: assertString(manifest.name, "name", entry),
        display_name: assertString(manifest.display_name, "display_name", entry),
        description: assertString(manifest.description, "description", entry),
        command: assertString(manifest.command, "command", entry),
        homepage:
          typeof manifest.homepage === "string" && manifest.homepage.length > 0
            ? manifest.homepage
            : undefined,
        platforms: assertStringArray(manifest.platforms, "platforms", entry),
        agent_friendly: Boolean(manifest.agent_friendly),
        supports_json: Boolean(manifest.supports_json),
        install_default: assertString(manifest.install?.default?.type, "install.default.type", entry),
        tags: assertStringArray(manifest.tags ?? [], "tags", entry),
        examples: assertStringArray(manifest.examples ?? [], "examples", entry),
      };
    })
  );
}

function renderFile(tools) {
  const allTags = [...new Set(tools.flatMap((tool) => tool.tags))].sort();

  return `// This file is generated by web/scripts/generate-registry.mjs.
// Do not edit it by hand.

import type { Tool } from "./registry";

export const tools: Tool[] = ${JSON.stringify(tools, null, 2)};

export const allTags: string[] = ${JSON.stringify(allTags, null, 2)};
`;
}

const tools = await loadTools();
const nextContent = renderFile(tools);
const currentContent = await fs.readFile(outputPath, "utf8").catch(() => null);

if (currentContent !== nextContent) {
  await fs.writeFile(outputPath, nextContent);
}
