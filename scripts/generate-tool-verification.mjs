import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..");
const manifestsDir = path.join(repoRoot, "manifests");
const resultsDir = path.join(repoRoot, "verification", "results");
const outputPath = path.join(repoRoot, "web", "src", "data", "tool-verification.generated.ts");

async function listToolIds() {
  return (await fs.readdir(manifestsDir))
    .filter((entry) => entry.endsWith(".yaml"))
    .map((entry) => entry.replace(/\.yaml$/, ""))
    .sort();
}

async function loadResults(toolIds) {
  const results = [];

  for (const toolId of toolIds) {
    const resultPath = path.join(resultsDir, `${toolId}.json`);

    try {
      const source = await fs.readFile(resultPath, "utf8");
      results.push(JSON.parse(source));
    } catch (error) {
      if (error && typeof error === "object" && "code" in error && error.code === "ENOENT") {
        continue;
      }

      throw error;
    }
  }

  return results.sort((a, b) => a.tool_id.localeCompare(b.tool_id));
}

function renderFile(results) {
  return `// This file is generated by scripts/generate-tool-verification.mjs.
// Do not edit it by hand.

import type { ToolVerificationResult } from "./tool-verification";

export const toolVerificationResults: ToolVerificationResult[] = ${JSON.stringify(results, null, 2)};
`;
}

const toolIds = await listToolIds();
const results = await loadResults(toolIds);
const nextContent = renderFile(results);
const currentContent = await fs.readFile(outputPath, "utf8").catch(() => null);

if (currentContent !== nextContent) {
  await fs.writeFile(outputPath, nextContent);
}
